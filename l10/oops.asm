.ORIG x3000

LEA R0, KBISR
STI R0, KBISRLOC
LD R0, SRITREN
STI R0, KBSR
LD R0, DOT
INFLOOP	OUT
	BRnzp INFLOOP

KBISR
	ST R0, ISR_R0_SAVE

	LDI R0, KBDR
	OUT
	
	LD R0, ISR_R0_SAVE
	RTI

ISR_R0_SAVE .FILL #0
KBSR .FILL xFE00
KBDR .FILL xFE02
DSR  .FILL xFE04
DDR  .FILL xFE06
KBISRLOC .FILL x0180
SRITREN .FILL x4000
DOT .STRINGZ "."	

.END
